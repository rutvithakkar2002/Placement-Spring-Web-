package com.bean;

public class StudentBean {

	private int studentId;
	private String firstname;
	private String lastname;
	private String gender;
	private String email;
	private double ssc;
	private double hsc;
	private double bachelorcgpa;
	private String university;
	private String branch;
	private String contactnum;

	public int getStudentId() {
		return studentId;
	}

	public void setStudentId(int studentId) {
		this.studentId = studentId;
	}

	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getLastname() {
		return lastname;
	}

	public void setLastname(String lastname) {
		this.lastname = lastname;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public double getSsc() {
		return ssc;
	}

	public void setSsc(double ssc) {
		this.ssc = ssc;
	}

	public double getHsc() {
		return hsc;
	}

	public void setHsc(double hsc) {
		this.hsc = hsc;
	}

	public double getBachelorcgpa() {
		return bachelorcgpa;
	}

	public void setBachelorcgpa(double bachelorcgpa) {
		this.bachelorcgpa = bachelorcgpa;
	}

	public String getUniversity() {
		return university;
	}

	public void setUniversity(String university) {
		this.university = university;
	}

	public String getBranch() {
		return branch;
	}

	public void setBranch(String branch) {
		this.branch = branch;
	}

	public String getContactnum() {
		return contactnum;
	}

	public void setContactnum(String contactnum) {
		this.contactnum = contactnum;
	}
}
